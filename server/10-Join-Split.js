/*El método join une los elementos del array, mediante una separación, y retorna un string. Si un elemento es undefined o null, se convierte en una cadena vacía.*/

/*Array de elementos contruido para la realización del ejemplo*/
const elements = ["Fire","Air","Water"];

/*Ejemplo de la implementación de join para unir los elementos del array*/
const ejemploJoin = elements.join("-");

/*Impresión por consola del resultado del anterior ejemplo*/
console.log(ejemploJoin);

